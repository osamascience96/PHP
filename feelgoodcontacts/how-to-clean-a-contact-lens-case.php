			<h5 class="contact_lense_shop_heading">How to clean a contact lens case</h5>
			
			<img src="images/Guides/how-to-clean-a-contact-lens-case.png">
			
			<p><br>Multiple ways have been suggested to disinfect a contact lens case. Some have suggested boiling contact lens cases in order to disinfect them, however we would strongly advise against this. We would recommend you sterilize a contact lens case with an effective lens solution. This article includes everything you need to know about looking after your contact lens container as well as how often to change a contact lens case. </p>
			
			<h5 class="price_match_heading">Where can I store my contact lenses?</h5>
			
			<p>If you wear monthly contact lenses or two weekly contact lenses, using a contact lens container to store your lenses should be second nature to you by now. After taking your lenses out at the end of the day, you can always rest easy knowing they’re being kept safe in your contact lens container.</p>
			
			<h5 class="price_match_heading">How do I clean my contact lens container?</h5>
			
			<p>These handy tools need to be looked after to make sure they stay safe and hygienic for storing your contact lenses. A popular question is how often to change solution in a contact lens case. If you don’t clean your contact lens container properly after every use, you could be putting your contact lenses and eyes at risk from harmful bacteria. This can cause eye infections and irritation to the eyes.</p>
			
			<p>So, we’ve outlined a few steps for you to make sure you’re cleaning your contact lens containers properly:</p>
			
			<ol>
				<li>Empty the contact lens container and make sure there’s no leftover contact lens solution remaining.</li>
				<li>Scrub the inside of the case using a clean cloth or your finger. Just make sure you wash your hands first taking care to dry them thoroughly with a lint free towel afterwards.</li>
				<li>Rinse out the case and lid with fresh multi-purpose solution. A multi-purpose solution makes wearing contacts a lot easier as has many uses. Comfi All-in One Solution can not only be used to clean your lenses but will protect you from getting dry eye.</li>
				<li>Set your contact lens container face down on a clean tissue or cloth and allow to air dry naturally</li>
			
			</ol>
			
			<h5 class="price_match_heading">How often should I clean my contact lens container?</h5>
			
			
			
			<p>You should clean your contact lens container with fresh solution after every use to avoid harmful bacteria from spreading. Fortunately, most packs of multi-purpose contact lens solution come with accompanying contact lens containers, allowing you to switch every three months at no extra cost.</p>
			
						
			<p>Failing to clean and disinfect your contact lens holder can lead to various eye infections including acanthamoeba keratitis. This is an infection of the cornea and can be very painful. </p>
			
			<p>If you experience a painful irritation in your eyes, you should make sure to see your ophthalmologist. A doctor who specialises in ophthalmology and will be able to offer you a diagnosis for various eye problems. When testing for acanthamoeba, they may perform a corneal scrape and culture. </p>
			
			<h5 class="price_match_heading">Can I clean my contact lens container with water?</h5>
			
			<p>You should avoid cleaning your contact lens containers with water at all costs. Both purified water and tap water can contain bacteria that is harmful to the eye. Find out more about the dangers of contact lenses and water here. </p>
			
			<p>The only way to achieve a hygienic and safe clean of your contact lens container is with fresh solution.<br></p>
			
			<h5 class="price_match_heading">Is boiling contact lens cases ok?</h5>
			
			<p>Numerous articles found online have suggested that it is safe to boil your contact lens container in a pot of water. This is not the case and we strongly advise against this. </p>
			
			<p>Some articles suggest you sterilize a contact lens case by running it through the dishwasher. This procedure also uses water and is therefore not recommended due to the risk of bacteria likely to cause infections. <br></p>
			
			<p></p>
			
			<h5 class="price_match_heading">Can I clean my contact lens case with alcohol?</h5>
			
			<p>The use of strong chemicals is not recommended to clean your contact case. Whilst alcohol is often viewed as a disinfecting solution, alcohol may also contain impurities that will leave a residue. It's therefore not recommended as a contact lens case cleaner. </p>
			
			<h5 class="price_match_heading">Which solution should I use to clean my contact lens container?</h5>
			
			<p>Only multi-purpose solutions can ensure an effective and safe clean of your contact lens box. These solutions can make wearing contact lenses much easier as they are an all in one product designed to save time and money. </p>
			
			
			<p>Saline solution or wetting solutions won’t offer proper sanitation from bacteria. Whilst hydrogen peroxide solution is a disinfectant, it needs to be neutralised to avoid causing harm to the eye.</p>
			
			<h5 class="price_match_heading">How long can you use a contact lens case for?</h5>
			
			<p>It is recommended that you replace your contact lens container every three months even if it looks in tip top condition. The reason being that bacteria and microorganisms can produce biofilm. This is a substance which helps bacteria to hide from the disinfectant in your contact lens solution. The biofilm is not visible to the eye which is why it is important to replace your case every three months no matter what. </p>
			
