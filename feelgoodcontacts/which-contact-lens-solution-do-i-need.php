			<h5 class="price_match_heading">What are saline solutions?</h5>
			
			<p>Saline solutions are gentle, preservative-free contact lens solutions, comprised of a sterile mix of salt and water. These solutions are suitable for rinsing and storing your soft contact lenses. They will wash away any dirt or debris from the surface and rewet your lenses to enhance comfort.</p>
			
			<p>Due to their disinfectant-free design, saline solutions are not suitable for cleaning or disinfecting your lenses.Instead, you’ll need to use a contact lens cleaner prior to using saline. This will ensure your eyes don't suffer irritation. However, you’ll still find saline solution an effective product to use for the final steps of your contact lens cleaning routine.</p>
			
			<p>Saline solution is therefore a useful tool in your contact lens cleaning kit. If your contact lens picks dirt or dust during the day, remove the lens and rinse it saline solution.</p>
			
