			<h5 class="price_match_heading">What are rigid gas permeable contact lens solutions?</h5>
			
			<p>When cleaning rigid and gas permeable contact lenses, you'll need to use a rigid gas permeable contact lens solution.</p>
			
			<p>These contact lens solutions have been specifically designed to clean the surfaces of rigid gas permeable contact lenses. Rigid gas permeable lenses don’t feature any water in their design and are predominantly made from silicone. As a result, these lenses require a different type of solution for cleaning.</p>
			
			<p>Fortunately, you can find a range of RGP solutions tailored to different contact lens cleaning needs.</p>
			
