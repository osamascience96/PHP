			<h5 class="price_match_heading">Which is the best contact lens solution for sensitive eyes?</h5>
			
			<p>Sensitive eyes can experience irritation easily so it's best to find the right lens care product for you. Sensitive Eyes Plus Saline Solution is a quality product from Bausch & Lomb. It can be used to rinse your lenses after they have been cleaned and disinfected – usually with hydrogen peroxide solution. The gentle formula is ideal for those who suffer from dry eyes and can be used with all soft contact lenses including silicone hydrogel contacts. </p>
			
			<p>You'll find this solution will enable your vision to be crisp and clear when wearing contacts as well as increase your comfort in wearing them.</p>
			
			<h5 class="price_match_heading">What are hydrogen peroxide contact lens solutions?</h5>
			
			<p>Hydrogen peroxide contact lens solutions typically provide a deeper clean and achieve a higher level of hygiene than other solutions. They are also better suited to sensitive eyes.</p>
			
			<p>These solutions have been designed to take care of cleaning, disinfecting, rinsing and storing all soft contact lenses. They don’t contain the preservatives often featured in multi-purpose contact lens solutions. This makes them ideal for anyone allergic to preservatives, or for those with ultra-sensitive eyes. They will provide comfort to your everyday wearing and you'll be able to see clearly through your lenses.</p>
			
			<p>Hydrogen peroxide solutions come with a special contact lens case, featuring a basket to go inside the case. To clean your lenses with this solution, just place your lens in the basket and rinse with a few drops of solution. This should be done before adding the basket to your solution-filled storage case and leaving it overnight. </p>
			
			<p>Some of these specialised contact lens cases will have a built-in neutraliser. Neutralising the hydrogen peroxide solution ensures your contact lenses are safe to wear. Doing this also minimises the risk of irritation or discomfort. Solutions such as EasySept and AOsept Plus HydraGlyde offer these specialised contact lens cases. Oxysept 1 Step is accompanied by a supply of neutralising tablets to add to the solution-filled case. They work to neutralise your lenses, ensuring they’re safe to wear again.</p>
			
			
			
