			<h5 class="price_match_heading">Does contact lens solution expire?</h5>
			
			<p>Yes, contact lens solution always has an expiration date, regardless of whether it’s a multi-purpose, saline, hydrogen peroxide or RGP solution. </p>
			
			<p>Using contact lens solution after its expiration date can be hazardous due to contamination. This can lead to a build-up of bacteria or an infection, causing irritation to the eye.</p>
			
			<p>You should therefore always check the expiration date when you open your new bottle and know when your current bottle is going to expire.</p>
			
			<h5 class="price_match_heading">Which contact lens solution can I use to store my extended wear contact lenses?</h5>
			
			<p>If you need to remove your extended wear contact lenses you should make sure you store them in a contact lens case filled with fresh solution.</p>
			
			<p>You’ll need to use a cleaning and storing solution for your extended wear lenses before you put them back in. This will help to eliminate any dirt or protein build-up on the lens, as well as rewet and condition your lenses for a comfortable wear.</p>
			
			<p>For cleaning and storing extended wear contact lenses, you’ll need to use a multi-purpose solution, a hydrogen peroxide solution. You can also use a combination of both a contact lens cleaner and saline solution. Alternatively, if your extended wear lenses are RGP contact lenses, you can use a rigid gas permeable contact lens solution to clean and store them.</p>
			
			
