			<h5 class="contact_lense_shop_heading">Which contact lens solution do I need?</h5>
			
			<img src="images/Guides/which-contact-lens-solution-do-i-need.png" style="width: 100%;">
			
			<p><br>There are a variety of contact lens cleaning solutions including hydrogen peroxide and saline solutions, and multi purpose solution. You'll also find that contact solution ingredients vary and many opt for a preservative free contact lens solution. Saline solution for contacts usually offer this and tend to be a popular choice for rinsing and wetting soft lenses. </p>
			
			<p>If you wear monthly contact lenses, two weekly contact lenses, or yearly contact lenses, it’s important to take care of them properly.</p>
			
			<p>Two weekly or monthly contact lenses can be worn daily but must be removed at night. They'll then need to be cleaned thoroughly and stored using a contact lens solution. This will ensure they are fresh, clean and hygienic to wear the next morning. Your eyes will be less prone to irritation and you'll find more comfort in wearing your contact lenses. </p>
			
			<p>If you’re a rigid gas permeable contact lens wearer you can  find a range of solutions suited to clean your contacts.</p>
			
			<p>Most contact lens wearers opt for soft contact lenses. Wearing soft lenses means you have a bigger variety of options for your contact lens cleaning routine.</p>
			
			<h5 class="price_match_heading">What are multi-purpose contact lens solutions?</h5>
			
			<p>The most convenient of soft contact lens solutions are multi-purpose solutions. These offer an all-in-one product designed to clean, disinfect, rinse and store your contact lenses.</p>
			
			<p>Most are suitable for all soft contact lenses, however there are even multi-purpose contact lens solutions for rigid gas permeable contact lenses.</p>
			
			<p>Multipurpose solution is the best solution for those with a hectic, busy lifestyle. After a full day of wear, just remove your lenses, cover with multi-purpose solution and rub gently to loosen any dirt or debris. Once you have done this, you can rinse them again with fresh solution. You can then proceed to fill up your contact lens case with more solution and place your lenses inside to store overnight. Multi-purpose solution can even be used to rinse your contact lens case when you’ve taken your lenses out. There is no need for a separate cleaning solution or disinfection solution. </p>
			
			<p>An all-in-one product such as our multi-purpose contact lens solutions will save you time and effort to ensure that your lenses are clean and clear and that your eyes are free from irritation.<br></p>
			
